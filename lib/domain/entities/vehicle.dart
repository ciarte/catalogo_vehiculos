class Vehicle {
  final int id;
  final String brand;
  final String model;
  final int year;
  final int price;
  final String color;
  final String transmission;
  final int mileage;
  final String imageUrl;

  Vehicle({
    required this.id,
    required this.brand,
    required this.model,
    required this.year,
    required this.price,
    required this.color,
    required this.transmission,
    required this.mileage,
    required this.imageUrl,
  });
}
